import dpdata as dpd
from glob import glob
from datetime import date
import sys
import os

usg_str = "Dump the whole SCS dataset.\nUsage: launch inside SCS main directory\npython Code/Collect-Dataset.py"
check_iterations = os.path.isdir('Iterations')
check_reference = os.path.isdir('Reference')

if not check_iterations:
    print("Directory Iterations/ not found, please launch this script inside SCS main directory.")
    print(usg_str)
elif not check_reference:
    print("Directory Reference/ not found, please launch this script inside SCS main directory.")
    print(usg_str)
else:
    #Dataset paths
    init_sets_path = 'Reference/Dataset/Initial'
    if not os.path.isdir(init_sets_path):
        print("Directory %s does not exist, no initial dataset found!" %init_sets_path)
    elif not os.listdir(init_sets_path):
        print("Directory %s is empty, no initial dataset found!" %init_sets_path)
    else:
        #Load the initial dataset
        init_sets = glob(init_sets_path+'/*')
        print("Load initial dataset from paths:")
        ms = dpd.MultiSystems()
        for init_set in init_sets:
            print(init_set)
            data = dpd.LabeledSystem(init_set, fmt = 'deepmd/npy')
            ms.append(data)
        
        #Check for scs iterations and load SCS-generated dataset if possible
        scs_raws_path = 'Iterations/*/Exploration/*/RAW'
        raws = glob(scs_raws_path)
        if len(raws) == 0:
            print("No dataset generated by SCS found, proceeding to dump initial dataset...")
        else:
            #Load new data generated by SCS
            print("Load SCS-generated dataset from paths:")
            for raw in raws:
                print(raw)
                lab_sys = dpd.LabeledSystem(raw, fmt = 'deepmd/raw')
                ms.append(lab_sys)

        #Shuffle frames
        for lab_sys in ms:
            lab_sys.shuffle()

        #ms.systems is a dictionary of LabeledSystems
        final_dataset_path = 'Reference/Dataset/Final/' + str(date.today())
        nframes = ms.get_nframes()
        print("Dumping %d frames dataset to directory %s" %(nframes, final_dataset_path))
        os.makedirs(final_dataset_path, exist_ok=True)
        ms.to_deepmd_npy(final_dataset_path, set_size = 200)